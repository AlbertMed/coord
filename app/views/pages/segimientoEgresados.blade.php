   <div style="text-align:center"><br><h3> SEGUIMIENTO DE EGRESADOS</h3><b></div>
   	
   <span style="font-size: 17px;">
                               
   El seguimiento de egresados busca establecer un estudio  enfocado hacia la evaluación de la calidad de la formación ofrecida por la institución. Es una herramienta que retroalimenta a la Institución, con base en la situación laboral y profesional de sus egresados, para adecuar o promover sus programas educativos y carreras técnicas. 
   El seguimiento de egresados tiene como objetivo el obtener información válida, confiable y oportuna sobre el proceso de inserción laboral, tanto de su desempeño en el empleo como de su trayectoria profesional, con la finalidad de valorar el grado de impacto del CBT en el sector productivo y social.
   Este mecanismo de seguimiento es muy útil para conocer áreas de oportunidad e implementar estrategias de mejora durante la permanencia de los estudiantes y en su egreso.
   Se busca contar con un directorio y una base de datos actualizada y confiable de sus egresados, que nos permita saber en que ámbitos laborales se desarrollan, como ha sido su desempeño profesional y lo más importante, a través de sus opiniones, contar con una herramienta de diagnóstico que permita tener una planeación educativa en nuestro plantel.
    <div style="text-align:center"><br><h3>  Objetivo General</h3><b></div>
 
   Obtener información válida, confiable y oportuna sobre el proceso de inserción laboral y acceso a la educación superior, tanto de su desempeño en el empleo como de su trayectoria profesional, con la finalidad de valorar el grado de impacto del CBT en el sector productivo y social.
   <div style="text-align:center"><br><h3>  Objetivo Específico</h3><b></div>
   
   Fortalecer los procesos formativos e informativos de nuestros estudiantes para que estos mejoren e incrementen sus oportunidades de inserción en los ámbitos educativo o laboral.
