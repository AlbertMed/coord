                         <div class="row our-story">
                             <div class="col-md-8">
                                 <h3>¿Coordinación de Vinculación?</h3>
                              <p align="justify"> La Coordinación de vinculación, difusión, servicio social, prácticas profesionales, certificación de competencias, titulación, evaluación e investigación es parte de la estructura organizativa y funcional del plantel que propicia la interacción con los sectores de la producción de bienes y servicios local, regional y estatal (Manual de funciones, DGEMSyS); así como con Instituciones de Educación Superior (IES) que busca impulsar y fortalecer las actividades de vinculación; coordinar y supervisar los programas de servicio social, prácticas profesionales y titulación, así mismo desarrollar acciones tendientes a la difusión, evaluación institucional e investigación. </p>

     <br>
                              <h3>¿Qué es el Servicio Social?</h3>
                              <p align="justify">Es una actividad que se realiza en Instituciones Públicas (Dependencias), apoyando trabajos que benefician a la población en general.</p>
                              <br><h3>Características del Servicio Social</h3>
     <p align="justify">Duración: 480 hrs. durante 6 meses
     Asignación: La Coordinación de Vinculación, Titulación y Certificación de Competencias asigna los lugares o bien el mismo alumno busca éstos cuando no son suficientes.
     <br><br>Requisitos:
     <br><br>
     Ser alumno regular académicamente.<br>
     Estar afiliado a una Institución de Salud Pública (por parte de la escuela o por parte de sus padres)<br>
     Ser alumno responsable y disciplinado<br>
     Realizar los trámites oficiales correspondientes para la incorporación al Departamento de Servicio Social<br>
     Requisitar cartas de presentación<br>
     Entregar carta de aceptación a la dependencia<br>
     Requisitar  carta de asignación para el Departamento de Servicio Social<br>
     Requisitar formatos de informes y constancia de termino de Servicio Social<br>
     </p>
     <br>
     <h3>Trámites oficiales</h3>

     <h3>Presentación:</h3><p align="justify">Una vez que  tengas la Dependencia y el Nombre de la persona responsable para realizar el servicio social, deberás iniciar el procedimiento del registro con el llenado de las cartas de presentación.Formato. Posteriormente, llevarlo a la oficina de Vinculación para su revisión con los profesores responsables.

     En la Dependencia te entregarán la Carta de Aceptación que deberás entregar en la oficina de Vinculación con la copia firmada de recibido por la Dependencia de las cartas de presentación.</p>

      <br>

     <h3>Registro/autorización: </h3>
     <p align="justify">Para tu registro al Departamento de Servicio Social, deberás continuar el mismo  procedimiento anterior en el llenado de formatos.

     Entregarás con las firmas correspondientes a la oficina de Vinculación los siguientes documentos en las (cantidades) señaladas:
     Carta Presentación  (2)
     Registro/autorización (4)</p>

      <br>

     <h3>Informes y constancia</h3>

     <p align="justify">Si ya concluiste el Servicio Social, deberás hacer el llenado de los formatos y llevarlos a la oficina de Vinculación para su revisión con los profesores responsables; una vez que estén en limpio, solicitarás las firmas correspondientes y entregarás a la oficina de Vinculación los siguientes documentos en las (cantidades) señaladas:
     Constancia (1 en papel Opalina y 2 en papel normal)
     Informe trimestral de actividades (2 originales)

     Constancia de Término (2 originales)
     Informe final de actividades (2 originales)</p>
